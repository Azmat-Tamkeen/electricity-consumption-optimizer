function applyLanguage(lang) {
    const t = translations[lang] || translations['en'];
    
    // Map data-i18n attributes to translation keys
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            if (el.tagName === 'INPUT' && (el.type === 'text' || el.type === 'password' || el.type === 'email' || el.type === 'number')) {
                el.placeholder = t[key];
            } else {
                el.innerText = t[key];
            }
        }
    });

    // Special handling for elements with nested HTML or dynamic parts
    const welcomeEl = document.querySelector('[data-i18n-welcome]');
    if (welcomeEl && t['welcome']) {
        const username = welcomeEl.getAttribute('data-username');
        welcomeEl.innerHTML = `${t['welcome']}, ${username} 👋`;
    }

    // Theme toggle text update
    const themeBtn = document.getElementById('theme-btn');
    if (themeBtn) {
        const isLight = document.documentElement.getAttribute('data-theme') === 'light';
        themeBtn.innerText = isLight ? t['dark_mode'] : t['light_mode'];
    }

    // Update active state of language buttons if they exist
    const langBtns = document.querySelectorAll("#language .submit-btn");
    if (langBtns.length > 0) {
        langBtns.forEach(btn => {
            btn.style.border = "none";
            btn.style.opacity = "1";
        });
        const languages = ['en', 'hi', 'te', 'ur', 'es', 'fr'];
        const index = languages.indexOf(lang);
        if (index !== -1 && langBtns[index]) {
            langBtns[index].style.border = "2px solid var(--accent-color)";
        }
    }
}

function changeLanguage(lang) {
    localStorage.setItem('selectedLanguage', lang);
    applyLanguage(lang);
    // Reload dynamic sections to apply language
    if (typeof loadInsights === 'function') loadInsights();
    if (typeof loadSuggestions === 'function') loadSuggestions();
    if (typeof loadGraph === 'function') loadGraph();
    if (typeof loadComparison === 'function') loadComparison();
    if (typeof loadHistory === 'function') loadHistory();
}

// Initialize language on page load
window.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('selectedLanguage') || 'en';
    applyLanguage(savedLang);
});
